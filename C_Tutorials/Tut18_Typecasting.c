/*
# Typecasting Convert one data type to other data type.

==> Typecasting Syntax :
    (type) variable;

*/

#include <stdio.h>

int main()
{
   float a =3;
   float b= (float)54/5;
    printf("The value of a is %d \n",(int)a);
    printf("The value of b is %f\n",b);
    return 0;
}